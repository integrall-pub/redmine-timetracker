import type {
  Action,
  Endpoint
} from '../types'

const initialState = { url: '', valid: false }

export default function (
  state: Endpoint = initialState,
  action: Action
): Endpoint {
  switch (action.type) {
    case 'api-endpoint-set':
      return validate(action.endpoint)

    default:
      return state
  }
}

const validate = (endpoint: string): Endpoint => ({
  url: endpoint,
  valid: endpoint.match(urlMatch) !== null
})

